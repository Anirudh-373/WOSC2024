					<p class="sp_para">This talk presents a synopsis of Marine Robots developed at N.I.O. Goa since its inception as a Millenium Project in 2000. We consider two developments namely the AUV –Maya, and the Autonomous Vertical Profiler (AVP) , and a third on-going project on the SeaBed Resident Event Driven Profiling System (SREP) . The unstated aim is to not to claim any major achievements   but to present our developmental experiences   as lessons that have been learned from ground zero. </p>
					<p>There are some unexpected conclusions that we can draw from this review:</p>
					<ul>
						<li>Marine robots are mostly based on simple  physical concepts  –force (acceleration) , buoyancy, lift,  drag and torque. This encourages one to maintain simplicity in  design.</li>
						<li>When the technology is tied to a specific science problem  there is  ample scope for innovation.</li>
						<li>Different robots, in general,  can be constructed from basic building blocks of hardware and software.</li>
						<li>Navigation algorithms, acoustic communications, and simulation  apart from control  will always be the primary tools to building field worthy marine robots. </li>
						<li>Strategic  applications in the ocean  are a natural offshoot of marine robot technology .</li>
						<li>Lastly, there is an element of unexpected surprises  in using  robots for Oceanography.  Our presentation will touch upon aspects of design, implementation and the discovery/ exploration of processes detected in reservoirs and coastal waters. We shall also float ideas on how marine robotics can be used in understanding the ocean.</li>
