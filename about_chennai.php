		<p>Chennai is the capital city of the Indian state of Tamil Nadu. Chennaipattinam, Madrasapattinam are the other former names of the city. Located on the Coromandel Coast off the Bay of Bengal, it is a major commercial, cultural, economic and educational center in South India. It is also known as the "Cultural Capital of South India" and was declared as the most livable city in India by the Institute of Competitiveness on account of economic, social, security and medical conditions as parameters.</p>
